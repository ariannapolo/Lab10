package it.polito.tdp.porto.model;

public class Coautori {
	private int a1;
	private int a2;
	public Coautori(int a1, int a2) {
		super();
		this.a1 = a1;
		this.a2 = a2;
	}
	/**
	 * @return the a1
	 */
	public int getA1() {
		return a1;
	}
	/**
	 * @param a1 the a1 to set
	 */
	public void setA1(int a1) {
		this.a1 = a1;
	}
	/**
	 * @return the a2
	 */
	public int getA2() {
		return a2;
	}
	/**
	 * @param a2 the a2 to set
	 */
	public void setA2(int a2) {
		this.a2 = a2;
	}
	
	
}
